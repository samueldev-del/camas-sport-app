export function t(lang, key, vars = {}) {
  const dict = DICTS[lang] || FR;
  let str = dict[key] ?? FR[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(`{${k}}`, v);
  }
  return str;
}

